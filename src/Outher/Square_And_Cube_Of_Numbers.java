package Outher;

public class Square_And_Cube_Of_Numbers 
{

	public static void main(String[] args)
	{
		
	   // square of 1 to 100
		for (int i = 1; i <=5; i++) 
		{
			System.out.print(i*i+" ");
			
		
		}
		
		System.out.println();
		 // cube of 1 to 100
		   for (int i = 1; i <=5; i++) 
				{
					System.out.print(i*i*i+" ");
					
				}	
		

		//   Square of a number
		int num=5;
		double square = Math.pow(num, 2);
		System.out.println();
		System.out.println("Square of "+ num + " is: "+ square);
		
		
		//cube of a number
		
		int num1=4;
		double square1 = Math.pow(num1, 3);
		System.out.println("Square of "+ num1 + " is: "+ square1);
		
		 
	
    

}}
